export default {
    name: 'product',
    data() {
        return {
            product: [],
            productId: 0,
            recommendList: [],
            qty: 1,
            // rowId: '',
        }
    },
    methods: {
        async getProduct(productId) {
            const res = await this.callApi("get", "/get_product_details", {
                productId: productId
            });
            if (res.status === 200) {
                this.product = res.data;
            }
        },
        async getRecommendProducts(productId) {
            const res = await this.callApi("get", "/get_product_recommend", {
                productId: productId
            });
            if (res.status === 200) {
                this.recommendList = res.data;
            }
        },
        async addToCart(productId) {
            const res = await this.callApi("post", "/add_to_cart", {
                productId: productId,
                qty: this.qty
            });
            if (res.status === 200) {
                // this.rowId = res.data;
                // console.log(this.rowId);
                swal("Successful!", "Your product is added!", "success");
            }
        },
    },
    async created() {
        this.productId = this.$route.query.productId;
        // this.productId = this.$route.params.productId;
        this.getProduct(this.productId);
        this.getRecommendProducts(this.productId);
    }
}
